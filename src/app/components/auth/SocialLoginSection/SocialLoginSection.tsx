export default function SocialLoginSection() {
  const [googleLoading, setGoogleLoading] = useState(false);

  async function handleGoogleSignIn() {
    setGoogleLoading(true);
    try {
      await signIn("google", { callbackUrl: "/api/auth/social-complete" });
    } catch {
      setGoogleLoading(false);
    }
  }

  return (
    <div className={styles.wrapper}>
      <p className={styles.dividerText}>or continue with</p>
      <div className={styles.buttons}>
        <SocialButton
          icon={<GoogleIcon />}
          label={googleLoading ? "Signing in..." : "Google"}
          variant="google"
          onClick={handleGoogleSignIn}
        />
        <SocialButton icon={<AppleIcon />} label="Apple" variant="apple" />
        <SocialButton icon={<LinkedInIcon />} label="Linkedin" variant="linkedin" />
      </div>
    </div>
  );
}
